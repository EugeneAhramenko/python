"""
Formatting Methods
There are three string methods that can change the casing of a string. These are .lower(), .upper(), and .title().
"""

favorite_song = 'SmOoTH'
print(favorite_song.lower()) # => smooth
poem_title = "spring storm"
print(poem_title.title()) # => Spring Storm
poem_author = "William Carlos Williams"
print(poem_author.upper()) # => WILLIAM CARLOS WILLIAMS
